    function checkAccount() {
        const input = document.getElementById("accountInput").value;
        const resultTable = document.getElementById("resultTable").getElementsByTagName("tbody")[0];
        resultTable.innerHTML = "";  // Clear previous results
    
        const matchedAccount = accounts.find(account => account.name === input);
    
        if (matchedAccount) {
            const row = resultTable.insertRow();
            const cell1 = row.insertCell(0);
            const cell2 = row.insertCell(1);
            const cell3 = row.insertCell(2);
            cell1.textContent = matchedAccount.name;
            cell2.textContent = matchedAccount.nature;
            cell3.textContent = matchedAccount.group;
        } else {
            const row = resultTable.insertRow();
            const cell = row.insertCell(0);
            cell.colSpan = 3;
            cell.textContent = "حسابی با این نام یافت نشد.";
        }
    }